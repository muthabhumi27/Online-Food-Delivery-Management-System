DROP DATABASE IF EXISTS food_delivery_db;
CREATE DATABASE food_delivery_db;
USE food_delivery_db;

CREATE TABLE Customer (
    CustomerID INT AUTO_INCREMENT PRIMARY KEY,
    CustomerName VARCHAR(100) NOT NULL,
    CustomerEmail VARCHAR(100),
    Phone VARCHAR(15) UNIQUE
);
CREATE TABLE Address (
    AddressID INT AUTO_INCREMENT PRIMARY KEY,
    Street VARCHAR(200), City VARCHAR(100), Pincode VARCHAR(10),
    CustomerID INT, FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID)
);
CREATE TABLE Restaurant (
    RestaurantID INT AUTO_INCREMENT PRIMARY KEY,
    RestaurantName VARCHAR(100) NOT NULL,
    Location VARCHAR(200), ContactNumber VARCHAR(15)
);
CREATE TABLE FoodItem (
    FoodItemID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL, Price INT NOT NULL,
    Availability BOOLEAN DEFAULT TRUE,
    RestaurantID INT, FOREIGN KEY (RestaurantID) REFERENCES Restaurant(RestaurantID)
);
CREATE TABLE `Order` (
    OrderID INT AUTO_INCREMENT PRIMARY KEY,
    OrderDate DATE NOT NULL, TotalAmount INT DEFAULT 0,
    OrderStatus ENUM('Placed','Preparing','Out for Delivery','Delivered','Cancelled') DEFAULT 'Placed',
    CustomerID INT, RestaurantID INT,
    FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID),
    FOREIGN KEY (RestaurantID) REFERENCES Restaurant(RestaurantID)
);
CREATE TABLE OrderItem (
    OrderItemID INT AUTO_INCREMENT PRIMARY KEY,
    OrderID INT, FoodItemID INT, Quantity INT NOT NULL, ItemPrice INT NOT NULL,
    FOREIGN KEY (OrderID) REFERENCES `Order`(OrderID),
    FOREIGN KEY (FoodItemID) REFERENCES FoodItem(FoodItemID)
);
CREATE TABLE Payment (
    PaymentID INT AUTO_INCREMENT PRIMARY KEY,
    PaymentMethod ENUM('UPI','Card','Cash','Wallet') DEFAULT 'UPI',
    PaymentStatus ENUM('Paid','Pending','Failed') DEFAULT 'Pending',
    PaymentDate DATE, OrderID INT,
    FOREIGN KEY (OrderID) REFERENCES `Order`(OrderID)
);
CREATE TABLE DeliveryPartner (
    PartnerID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL, Phone VARCHAR(15),
    VehicleType ENUM('Bike','Scooter','Cycle') DEFAULT 'Bike',
    OrderID INT, FOREIGN KEY (OrderID) REFERENCES `Order`(OrderID)
);
CREATE TABLE Review (
    ReviewID INT AUTO_INCREMENT PRIMARY KEY,
    Rating INT CHECK (Rating BETWEEN 1 AND 5),
    Comment TEXT, ReviewDate DATE,
    CustomerID INT, RestaurantID INT,
    FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID),
    FOREIGN KEY (RestaurantID) REFERENCES Restaurant(RestaurantID)
);

